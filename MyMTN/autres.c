void logo(int lo,int la)
{
	int i,j,m=0;
	for(i=1;i<=lo;i++)
	{
		printf("\t\t");
		for(j=1;j<=la;j++)
		{
			if(j<=i||(la-j-m)<=0)
			printf("*");
			else
			printf(" ");
		}
		printf("\n");
		m++;
	}
}
